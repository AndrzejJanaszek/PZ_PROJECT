            <h2 class="blog_article__header">Cyprian Kamil Norwid</h2>
            <p class="blog_article__date">2021-04-15 18:22</p>
            <p class="blog_article__content">Cyprian Kamil Norwid, właściwie Cyprian Ksawery Gerard Walenty Norwid herbu Topór (ur. 24 września 1821 w Laskowie-Głuchach, zm. 23 maja 1883 w Paryżu) – polski poeta, prozaik, dramatopisarz, eseista, grafik, rzeźbiarz, malarz i filozof.

Często uznawany za ostatniego z czterech najważniejszych polskich poetów romantycznych. Wielu historyków literatury uważa jednak taki pogląd za zbytnie uproszczenie, zaliczając jego twórczość raczej do klasycyzmu i parnasizmu.

Przeważającą część swojego życia spędził za granicą, głównie w Paryżu, żyjąc w nędzy i utrzymując się z prac dorywczych. Twórczość Norwida, trudna do zrozumienia dla jemu współczesnych, została zapomniana po jego śmierci. Został odkryty ponownie dopiero w okresie Młodej Polski głównie za sprawą Zenona Przesmyckiego-Miriama (po części również młodego Władysława Stanisława Reymonta[1]).</p>
