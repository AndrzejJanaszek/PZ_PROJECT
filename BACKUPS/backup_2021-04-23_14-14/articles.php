            <h2 class="blog_article__header">Fryderyk Franciszek Chopin</h2>
            <p class="blog_article__date">2021-04-15 18:21</p>
            <p class="blog_article__content">Fryderyk Franciszek Chopin (fr. Frédéric Chopin; ur. 22 lutego lub 1 marca 1810↓ w Żelazowej Woli, zm. 17 października 1849 w Paryżu) – polski kompozytor i pianista. Od września 1831 roku mieszkał we Francji.

Jest uważany za jednego z najwybitniejszych kompozytorów romantycznych, a także za jednego z najważniejszych polskich kompozytorów w historii. Był jednym z najsłynniejszych pianistów swoich czasów, często nazywany poetą fortepianu. Elementem charakterystycznym dla utworów Chopina jest pogłębiona ekspresja oraz czerpanie z wzorców stylistycznych polskiej muzyki ludowej.

Z okazji 150-lecia urodzin Fryderyka Chopina uchwałą Rady Państwa z 8 maja 1958 rok 1960 został ogłoszony Rokiem Chopinowskim[1]. Z okazji jubileuszu 200-lecia urodzin Chopina zarówno uchwałą Sejmu RP z 9 maja 2008[2], jak i uchwałą Senatu RP z 7 października 2009[3], rok 2010 został ustanowiony Rokiem Fryderyka Chopina.</p>
