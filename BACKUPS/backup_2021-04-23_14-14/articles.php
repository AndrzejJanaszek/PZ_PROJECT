            <h2 class="blog_article__header">Juliusz Słowacki</h2>
            <p class="blog_article__date">2021-04-15 18:22</p>
            <p class="blog_article__content">Juliusz Słowacki herbu Leliwa (ur. 4 września 1809 w Krzemieńcu, zm. 3 kwietnia 1849 w Paryżu[1]) – polski poeta, przedstawiciel romantyzmu, dramaturg i epistolograf. Obok Mickiewicza i Krasińskiego określany jako jeden z Wieszczów Narodowych. Twórca filozofii genezyjskiej (pneumatycznej), epizodycznie związany także z mesjanizmem polskim, był też mistykiem. Obok Mickiewicza uznawany powszechnie za największego przedstawiciela polskiego romantyzmu.

Utwory Słowackiego, zgodnie z duchem epoki i ówczesną sytuacją narodu polskiego, podejmowały istotne problemy związane z walką narodowowyzwoleńczą, z przeszłością narodu i przyczynami niewoli, ale także poruszały uniwersalne tematy egzystencjalne. Jego twórczość wyróżniała się mistycyzmem, wspaniałym bogactwem wyobraźni, poetyckich przenośni i języka. Jako liryk zasłynął pieśniami odwołującymi się do Orientu, źródeł ludowych i słowiańszczyzny. Był poetą nastrojów, mistrzem operowania słowem. Obok Cypriana Kamila Norwida i Tadeusza Micińskiego uważany za największego z mistyków polskiej poezji. Miał zresztą (i opisał je w Raptularzu[2]) doświadczenia mistyczne.</p>
